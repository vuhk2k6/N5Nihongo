def vocab(seq, lid, k, f, r, m, t):
    """Build a vocabulary entry. seq starts at 1 within the lesson."""
    return {
        "wordId": lid * 100 + seq,
        "lessonId": lid,
        "kanji": k,
        "furigana": f,
        "romaji": r,
        "meaning": m,
        "type": t,
    }


def grammar(seq, lid, title, structure, explanation, examples):
    return {
        "grammarId": lid * 100 + seq,
        "lessonId": lid,
        "title": title,
        "structure": structure,
        "explanation": explanation,
        "examples": [
            {"japanese": ex[0], "vietnamese": ex[1]} for ex in examples
        ],
    }


def kanji(seq, lid, ch, on, kun, m, sc):
    return {
        "kanjiId": lid * 100 + seq,
        "lessonId": lid,
        "character": ch,
        "onyomi": on,
        "kunyomi": kun,
        "meaning": m,
        "strokeCount": sc,
    }


